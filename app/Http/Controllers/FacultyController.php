    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'facultyName'    => 'required|string',
            'facultyInitial' => 'required|string',
        ]);

        $fac = Faculty::create([
            'uuid'            =>  Str::orderedUuid(),
            'faculty_name'    =>  $request->facultyName,
            'faculty_initial' =>  $request->facultyInitial,
        ]);

        if ($fac) {
            return redirect()->back()->with('success','Faculty created successfully.');
        } else {
            return redirect()->back()->with('error','Something wents wrong, try again.');
        }
    }

